#include <QMC5883LCompass.h>

/* **************************************************************** */
/* * Global Variables                                             * */
/* **************************************************************** */ 
QMC5883LCompass Compass; 
double Compass_Value = 0;


/* **************************************************************** */
/* * Setup Routine                                                * */
/* **************************************************************** */ 
void Compass_setup() {
  Compass.init();
}


/* **************************************************************** */
/* * Loop Routine                                                 * */
/* **************************************************************** */ 
void Compass_loop() {
  Compass.read();
  Compass_Value = (double)Compass.getAzimuth();
}
